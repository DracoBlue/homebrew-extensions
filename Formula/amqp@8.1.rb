  head "https://github.com/php-amqp/php-amqp"
  license "PHP-3.01"

  bottle do
    root_url "https://dl.bintray.com/shivammathur/extensions"
    rebuild 17
    sha256 arm64_big_sur: "cd146eeb947534e34a13b3a5ad73123728ac22d8a2b76896f53861b7957848cc"
    sha256 big_sur:       "c4e917ab932009b901d9f7999c8468bec9de305ee9871eccc62c7bf59f1a59e5"
    sha256 catalina:      "1d2bb2b21f27ed0142a3e7b9ca9d9b3f79fcc2a1314f8ff1d7f34703254cd886"
  end

  depends_on "rabbitmq-c"

  def install
    args = %W[
      --with-amqp=shared
      --with-librabbitmq-dir=#{Formula["rabbitmq-c"].opt_prefix}
    ]
    patch_spl_symbols
    safe_phpize
    system "./configure", "--prefix=#{prefix}", phpconfig, *args
    system "make"
    prefix.install "modules/#{module_name}.so"
    write_config_file
  end
end
