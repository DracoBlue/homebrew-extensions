  sha256 "c9b27857fc7e39654518af5c35eb947b371556375550412b7166f75c03c8b5c8"
  head "https://github.com/php-amqp/php-amqp"
  license "PHP-3.01"

  bottle do
    root_url "https://dl.bintray.com/shivammathur/extensions"
    rebuild 18
    sha256 arm64_big_sur: "d6cd909b306d0568abac58e12b38782d2c975ab7b71531464a0ac74645a67d00"
    sha256 big_sur:       "03deaff532c3faeb0aa9803df0e85ade31a406af736c260e3ee9158a7b83b024"
    sha256 catalina:      "e2e5196f9651f899d844ce6433845b9e9021754a6502334b5847a6452b6b8fb7"
  end

  depends_on "rabbitmq-c"

  def install
    args = %W[
      --with-amqp=shared
      --with-librabbitmq-dir=#{Formula["rabbitmq-c"].opt_prefix}
    ]
    patch_spl_symbols
    safe_phpize
    system "./configure", "--prefix=#{prefix}", phpconfig, *args
    system "make"
    prefix.install "modules/#{module_name}.so"
    write_config_file
  end
end
